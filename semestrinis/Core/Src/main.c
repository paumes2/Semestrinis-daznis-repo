void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef* htim)
{
    if(state == IDLE)
    {
        T1 = TIM2->CCR1;
        TIM2_OVC = 0;
        state = DONE;
    }
    else if(state == DONE)
    {

        T2 = TIM2->CCR1;
        if(T2>T1){
        ticks = (T2 + (TIM2_OVC * 65536)) - T1;
        frequency = ((F_CLK/ticks));
				frequency = frequency/5;
        }
        state = IDLE;
    }
}

